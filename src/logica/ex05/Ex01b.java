package logica.ex05;

import br.senai.sp.info132.console.Programa;

public class Ex01b extends Programa {
	public void inicio() {
		for (int i = 2, col = 1; i <= 1000; i += 2, col++) {
			if(col < 20) {
				escreva(String.format("%04d ", i));
			} else {
				escrevaL(String.format("%04d ", i));
				col = 0;
			}
		}
	}
}
